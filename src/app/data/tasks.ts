export type TaskStatus =
  | "Selesai"
  | "Dalam Proses"
  | "Belum Mula";
export type TaskType = "percent" | "count";

export interface Task {
  id: number;
  bil: number;
  aktiviti: string;
  sasaran: number;
  unit: string;
  type: TaskType;
  pencapaian: number;
  peratusan: number;
  status: TaskStatus;
}

export interface Section {
  id: string;
  name: string;
  fullName: string;
  color: string;
  gradient: string;
  tasks: Task[];
}

function calcPercent(
  pencapaian: number,
  sasaran: number,
  type: TaskType,
): number {
  if (type === "percent") return pencapaian;
  return sasaran > 0
    ? Math.round((pencapaian / sasaran) * 100)
    : 0;
}

function status(pct: number): TaskStatus {
  if (pct >= 100) return "Selesai";
  if (pct > 0) return "Dalam Proses";
  return "Belum Mula";
}

function t(
  id: number,
  bil: number,
  aktiviti: string,
  sasaran: number,
  unit: string,
  type: TaskType,
  pencapaian: number,
): Task {
  const pct = calcPercent(pencapaian, sasaran, type);
  return {
    id,
    bil,
    aktiviti,
    sasaran,
    unit,
    type,
    pencapaian,
    peratusan: Math.min(pct, 100),
    status: status(pct),
  };
}

